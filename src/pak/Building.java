package pak;

public class Building
{
	
	private String id;
	private String name;
	private int health;
	
	public Building(String id, String name, int health)
	{
		super();
		this.id = id;
		this.name = name;
		this.health = health;
	}

	public String getId()
	{
		return id;
	}

	public String getName()
	{
		return name;
	}

	public int getHealth()
	{
		return health;
	}
	
	
	
	
	

}
